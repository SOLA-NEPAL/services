package org.sola.services.ejb.address.repository.entities;

import javax.persistence.Column;
import javax.persistence.Id;
import javax.persistence.Table;
import org.sola.services.common.repository.entities.AbstractVersionedEntity;

/**
 * Entity representing the address.address table. 
 * @author soladev
 */
@Table(name = "address", schema = "address")
public class Address extends AbstractVersionedEntity {

    @Id
    @Column(name = "id")
    private String id;
    @Column(name = "description")
    private String description;
    @Column(name = "ext_address_id")
    private String extAddressId;
    //additional fields.
    @Column(name="street")
    private String street;
    @Column(name="districtcode")
    private String districtCode;
    @Column(name="vdc_code")
    private String vdcCode;
    @Column(name="ward_no")
    private String wardNo;
    
    public String getVdcCode() {
        return vdcCode;
    }

    public void setVdcCode(String vdcCode) {
        this.vdcCode = vdcCode;
    }
    
    public String getWardNo() {
        return wardNo;
    }

    public void setWardNo(String wardNo) {
        this.wardNo = wardNo;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getDistrictCode() {
        return districtCode;
    }

    public void setDistrictCode(String districtCode) {
        this.districtCode = districtCode;
    }
    
    public Address() {
        super();
    }

    public String getId() {
        id = id == null ? generateId() : id;
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getExtAddressId() {
        return extAddressId;
    }

    public void setExtAddressId(String extAddressId) {
        this.extAddressId = extAddressId;
    }
}
